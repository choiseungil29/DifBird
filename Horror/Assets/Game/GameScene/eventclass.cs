using UnityEngine;
using System.Data;
using System.Collections;
using Mono.Data.Sqlite;

public class eventclass
{
    private bool flag;
    private string Name;
    private string Character;
    private string Discription;
	// Use this for initialization
    public eventclass()
    {
    }

    public eventclass(string name, string character, string discription)
    {
        this.flag = true;
        this.Name = name;
        this.Character = character;
        this.Discription = discription;
    }
    public string getName()
    {
        return Name;
    }
    public string getCharacter()
    {
        return Character;
    }
    public string getDiscription()
    {
        return Discription;
    }
    public bool getFlag()
    {
        return flag;
    }
    public void setFlag(bool s)
    {
        flag = s;
    }
}
